def inclui_lado(nome, posicao):
  
  if posicao != 2:
    return posicao
  else:
    try:
      informacoes_jogador = wikipedia.page(nome).content

      if 'right back' in informacoes_jogador:
        return 7
      elif 'left back' in informacoes_jogador:
        return 8
      elif 'right' in informacoes_jogador and 'left' not in informacoes_jogador:
        return 7
      elif 'left' in informacoes_jogador and 'right' not in informacoes_jogador:
        return 8
      else:
        return 2
    except wikipedia.exceptions.PageError:
      return 2
    except KeyError:
      return 2 

