def extrai_total_porcentagem(x):
  parcelas = x.split(' (')

  acertados = float(parcelas[0])
  acuracia = round(converte_para_float(parcelas[1].split('%)')[0])/100,2)
  if acuracia != 0:
    total = float(acertados)/acuracia
  else:
    total = 0

  return int(round(total,0))
